    // Get all projects
    getAllProjects() {
        return this.projects;
    },

    // Get projects by category
    getProjectsByCategory(category) {
        if (category === 'all') {
            return this.projects;
        }
        return this.projects.filter(project => project.category === category);
    },

    // Get project by ID
    getProjectById(id) {
        return this.projects.find(project => project.id === parseInt(id));
    },

    // Get next project
    getNextProject(currentId) {
        const currentIndex = this.projects.findIndex(project => project.id === parseInt(currentId));
        const nextIndex = (currentIndex + 1) % this.projects.length;
        return this.projects[nextIndex];
    },

    // Get previous project
    getPreviousProject(currentId) {
        const currentIndex = this.projects.findIndex(project => project.id === parseInt(currentId));
        const prevIndex = currentIndex === 0 ? this.projects.length - 1 : currentIndex - 1;
        return this.projects[prevIndex];
    },

    // Get random projects
    getRandomProjects(count = 3, excludeId = null) {
        let availableProjects = this.projects;
        if (excludeId) {
            availableProjects = this.projects.filter(project => project.id !== parseInt(excludeId));
        }
        
        const shuffled = [...availableProjects].sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    },

    // Get categories
    getCategories() {
        const categories = [...new Set(this.projects.map(project => project.category))];
        return ['all', ...categories];
    },

    // Search projects
    searchProjects(query) {
        const searchQuery = query.toLowerCase();
        return this.projects.filter(project => 
            project.title.toLowerCase().includes(searchQuery) ||
            project.subtitle.toLowerCase().includes(searchQuery) ||
            project.description.toLowerCase().includes(searchQuery) ||
            project.tags.some(tag => tag.toLowerCase().includes(searchQuery))
        );
    }
